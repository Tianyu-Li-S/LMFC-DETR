class PRConv(PConv):
    def __init__(self, dim, n_div=4, forward='split_cat'):
        super().__init__(dim, n_div, forward)
        
        self.PConv = RepConv(self.dim_conv3, self.dim_conv3, k=3, act=False, bn=False)
